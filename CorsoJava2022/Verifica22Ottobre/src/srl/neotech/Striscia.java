package srl.neotech;

public class Striscia {

	private Integer lunghezza;
	private Integer altezzaErba;
	private Integer numeroStriscia;
	
	public Integer getLunghezza() {
		return lunghezza;
	}
	public void setLunghezza(Integer lunghezza) {
		this.lunghezza = lunghezza;
	}
	public Integer getAltezzaErba() {
		return altezzaErba;
	}
	public void setAltezzaErba(Integer altezzaErba) {
		this.altezzaErba = altezzaErba;
	}
	public Integer getNumeroStriscia() {
		return numeroStriscia;
	}
	public void setNumeroStriscia(Integer numeroStriscia) {
		this.numeroStriscia = numeroStriscia;
	}

	
}
